impl UiStrings {
    pub fn songs_count_label(&self, count: usize) -> String {
        format!("{} {}", count, self.songs_label)
    }

    pub fn artists_count_label(&self, count: usize) -> String {
        format!("{} {}", count, self.artists_label)
    }

    pub fn genres_count_label(&self, count: usize) -> String {
        format!("{} {}", count, self.genres_label)
    }

    pub fn albums_count_label(&self, count: usize) -> String {
        format!("{} {}", count, self.albums_label)
    }

    pub fn folders_count_label(&self, count: usize) -> String {
        format!("{} {}", count, self.folders_label)
    }

    pub fn tracks_count_label(&self, count: usize) -> String {
        format!("{} {}", count, self.tracks_label)
    }

    pub fn scan_folder_label(&self, folder: impl std::fmt::Display) -> String {
        format!("{}{}", self.scan_folder_prefix, folder)
    }

    pub fn preview_theme_label(
        &self,
        theme: &str,
        accent: &str,
        density: &str,
    ) -> String {
        format!(
            "{}: {} · {}: {} · {}: {}",
            self.preview_theme_label,
            theme,
            self.preview_accent_label,
            accent,
            self.preview_density_label,
            density
        )
    }

    pub fn preview_text_label(&self, text: &str, effects: &str, animations: &str) -> String {
        format!(
            "{}: {} · {}: {} · {}: {}",
            self.preview_text_label,
            text,
            self.preview_effects_label,
            effects,
            self.preview_animations_label,
            animations
        )
    }

    pub fn load_more_label(&self, remaining: usize) -> String {
        format!(
            "{} ({} {})",
            self.load_more_label, remaining, self.remaining_label
        )
    }

    pub fn playlist_move_prompt(&self, index: usize) -> String {
        format!("{} {:02}. {}", self.playlist_move_prefix, index, self.playlist_move_suffix)
    }
}

